public class RoomUpgrades {

    String name;
    double cost;

    public RoomUpgrades() {
        this.name = "";
        this.cost = 0;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public double getCost() {
        return cost;
    }

    public void setCost(double cost) {
        this.cost = cost;
    }
}
